/**
 * C++ program to demonstrate the use of various primitive data types.
 */

#include <iostream>
using namespace std;

int main() {
    // integer data type
    int integer = 10;
    cout << "Integer: " << integer << endl;

    // character data type
    char character = 'A';
    cout << "Character: " << character << endl;

    // boolean data type
    bool boolean = true;
    cout << "Boolean: " << boolean << endl;

    // floating point data type
    float floating = 3.14;
    cout << "Floating: " << floating << endl;

    // double data type
    double doubleValue = 3.14159;
    cout << "Double: " << doubleValue << endl;

    return 0;
}